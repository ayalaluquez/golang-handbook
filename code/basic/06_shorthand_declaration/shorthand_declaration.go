package main

import "fmt"

func main() {
	// declaring integer
	num := 12
	fmt.Printf("The type of variable 'num' is: %T.\n", num)

	// declaring float
	decimal := 15.45
	fmt.Printf("The type of variable 'decimal' is: %T.\n", decimal)

	// declaring string
	name := "Octallium"
	fmt.Printf("The type of variable 'name' is : %T.\n", name)
}
